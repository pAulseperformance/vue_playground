<template>
  <form @submit.prevent="addTodo">
    <input type="text" v-model="title" name="title" placeholder="Add Todo..." />
    <input type="submit" value="Submit" class="btn" />
  </form>
</template>

<script>
export default {
  name: "AddTodo",
  methods: {
    addTodo() {
      const newTodo = {
        id: Date.now(),
        title: this.title,
        completed: false
      };
      this.$emit("add-todo", newTodo);
      this.title = "";
    }
  },

  data() {
    return {
      title: ""
    };
  }
};
</script>

<style>
form {
  display: flex;
}
input[type="text"] {
  flex: 10;
  padding: 5px;
}
input[type="submit"] {
  flex: 2;
}
</style>